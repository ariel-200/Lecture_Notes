package Week2;

public class HelloForLoop {
    public static void main(String[] args) {

     /* Working With Loops
        For Loops - for doing something a number of times
        While Loops - repeating while something is true (or false) or
        until something is true (or false)
      */

        //        Create loop      Condition   What to do after each loop
        for (      int x = 0   ;    x < 10    ;        x++) {
            System.out.println(x);

        }

        /*
        x = x+2 - count by 2s
        x = x+5 - count by 5s
        x = x-1 or x-- count down
         */
    }

}
