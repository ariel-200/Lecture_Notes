package Week0;

public class Variables {
    public static void main(String[] args) {

        /*
        This is used to make
        double lined comments
        for longer comments.
         */

        // Create a String variable for my name and print
        String name = "Ariel";
        System.out.println(name);

        // Create a String variable for college and print
        String college = "MCTC";
        System.out.println(college);

        System.out.println("My name is " + name + " and my school is " + college);

        // Create an int variable
        int javaCredits = 6;
        System.out.println("The Java class is " + javaCredits + " credits");

        //Blank line
        System.out.println();

        //Create a double variable, for floating-point numbers
        double todayTemp = 70.5;
        System.out.println("The high temperature today will be " + todayTemp + "F");

        /*
        \t becomes a tab
        \n becomes a newline
        \" becomes a double quote
        \\ to use a backslash in a String
         */


    }
}
